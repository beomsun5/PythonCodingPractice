score = int(input())
if(score>=90): print("A")   # Print A if the score is between 90 and 100
elif(score>=80): print("B") # Print B if the score is between 80 and 89
elif(score>=70): print("C") # Print C if the score is between 70 and 79
elif(score>=60): print("D") # Print D if the score is between 60 and 69
else: print("F")            # Print F if the score is under 60